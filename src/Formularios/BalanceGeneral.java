    public void mostrarEstadosFinancieros() {
        // Mostrar todos los estados Financieros:
        int id_catalogo = Integer.parseInt(idCatalogo.getText());

        op.IngresosEstadoResultado(jtIngresosEstadoResultado, id_catalogo);
        op.GastosEstadoResultado(jtGastosEstadoResultados, id_catalogo);

        jlTotalIngresos.setText("$" + String.valueOf(op.getTotalIngresos()));
        jlTotalGastos.setText("$" + String.valueOf(op.getTotalGastos()));
        BigDecimal utilidad = new BigDecimal(String.valueOf(op.getTotalIngresos() - op.getTotalGastos()));

        utilidad = utilidad.setScale(2, RoundingMode.HALF_UP);
        jlUtilidadEstadoResultado.setText("$" + utilidad);
        jlUtilidad.setText("$" + utilidad);

        double valors = 0;
        double u = op.getTotalIngresos();
    
        for(int i=0;i<jtIngresosEstadoResultado.getRowCount();i++){
        valors = ((Double.parseDouble(jtIngresosEstadoResultado.getModel().getValueAt(i, 1).toString())*100)/u);
        jtIngresosEstadoResultado.getModel().setValueAt((df.format(valors)+"%"),i ,2);
        }
        
        for(int i=0;i<jtGastosEstadoResultados.getRowCount();i++){
        valors = ((Double.parseDouble(jtGastosEstadoResultados.getModel().getValueAt(i, 1).toString())*100)/u);
        jtGastosEstadoResultados.getModel().setValueAt((df.format(valors)+"%"),i ,2);
        }
         
        lblIngresosAV.setText(df.format((op.getTotalIngresos()*100)/u)+"%");
        lblGastosAV.setText(df.format((op.getTotalGastos()*100)/u)+"%");
        lblUtilidadAV.setText(df.format((utilidad.doubleValue()*100)/u)+"%");
        
        
        op.VariacionCapital(jtCapitalesEstadoResultado, id_catalogo);
        op.variacionCapitalRetiros(jtRetiros, id_catalogo);

        BigDecimal saldoRetiros = new BigDecimal(String.valueOf(op.getTotalRetiros()));
        jlTotalRetiros.setText("$" + saldoRetiros);
        BigDecimal saldoCapital = new BigDecimal(String.valueOf(op.getSaldoCapital()));
        BigDecimal capital = utilidad.add(saldoCapital).subtract(saldoRetiros);

        capital = capital.setScale(2, RoundingMode.HALF_UP);
        jlTotalCapital.setText("$" + capital);

        //Para el BG
        op.balanceGeneralActivosCorrientes(jtActivosCorrientesBG, id_catalogo);
        BigDecimal saldoActivosCorrientesBG = new BigDecimal(String.valueOf(op.getSaldoActivosCorrientes()));
        saldoActivosCorrientesBG = saldoActivosCorrientesBG.setScale(2, RoundingMode.HALF_UP);
        lblTotalActivosCorrientesBG.setText("$" + saldoActivosCorrientesBG);

        op.balanceGeneralActivosFijos(jtActivosFijosBG, id_catalogo);
        BigDecimal saldoActivosFijosBG = new BigDecimal(String.valueOf(op.getSaldoActivosFijos()));
        saldoActivosFijosBG = saldoActivosFijosBG.setScale(2, RoundingMode.HALF_UP);
        lblTotalActivosFijosBG.setText("$" + saldoActivosFijosBG);

        lblTotalActivosBG.setText("$" + (saldoActivosCorrientesBG.doubleValue() + saldoActivosFijosBG.doubleValue()));

        op.balanceGeneralPasivosCorrientes(jtPasivosCorrientesBG, id_catalogo);
        BigDecimal saldoPasivosCorrientesBG = new BigDecimal(String.valueOf(op.getSaldoPasivosCorrientes()));
        saldoPasivosCorrientesBG = saldoPasivosCorrientesBG.setScale(2, RoundingMode.HALF_UP);
        lblTotalPasivosCorrientesBG.setText("$" + saldoPasivosCorrientesBG);
        capital = capital.add(saldoPasivosCorrientesBG);

        op.balanceGeneralPasivosFijos(jtPasivosFijosBG, id_catalogo);
        BigDecimal saldoPasivosFijosBG = new BigDecimal(String.valueOf(op.getSaldoPasivosFijos()));
        saldoPasivosFijosBG = saldoPasivosFijosBG.setScale(2, RoundingMode.HALF_UP);
        lblTotalPasivosFijosBG.setText("$" + saldoPasivosFijosBG);
        capital = capital.add(saldoPasivosFijosBG);

        lblTotalPasivosBG.setText("$" + (saldoPasivosCorrientesBG.doubleValue() + saldoPasivosFijosBG.doubleValue()));

        
        op.balanceGeneralCapital(jtCapitalContableBG, id_catalogo);
        BigDecimal saldoCapitalContableBG = new BigDecimal(String.valueOf(op.getSaldoCapitalAV()));
        saldoCapitalContableBG = saldoCapitalContableBG.setScale(2, RoundingMode.HALF_UP);
        lblTotalCapitalBG.setText("$" + saldoCapitalContableBG);
        capital = capital.add(saldoCapitalContableBG);
        
//---------------------------------

        
        //Analsis vertical
        double p = saldoActivosCorrientesBG.doubleValue() + saldoActivosFijosBG.doubleValue();
              
                
        lblTotalActivosAV.setText("100%");
        lblPasivoCapitalAV.setText("100%");
        lblActivosCorrientesAV.setText(df.format((saldoActivosCorrientesBG.doubleValue()*100)/p)+"%");
        lblActivosFijosAV.setText(df.format((saldoActivosFijosBG.doubleValue()*100)/p)+"%");
        lblPasivosCorrientesAV.setText(df.format((saldoPasivosCorrientesBG.doubleValue()*100)/p)+"%");
        lblPasivosFijosAV.setText(df.format((saldoPasivosFijosBG.doubleValue()*100)/p)+"%");
        lblCapitalContableAV.setText(df.format((saldoCapitalContableBG.doubleValue()*100)/p)+"%");
        double valor = 0;
               
        for(int i=0;i<jtActivosCorrientesBG.getRowCount();i++){
        valor = ((Double.parseDouble(jtActivosCorrientesBG.getModel().getValueAt(i, 1).toString())*100)/p);
        jtActivosCorrientesBG.getModel().setValueAt((df.format(valor)+"%"),i ,2);
        }
        
               
        for(int i=0;i<jtActivosFijosBG.getRowCount();i++){
        valor = ((Double.parseDouble(jtActivosFijosBG.getModel().getValueAt(i, 1).toString())*100)/p);
        jtActivosFijosBG.getModel().setValueAt((df.format(valor)+"%"),i ,2);
        }
        
        for(int i=0;i<jtPasivosCorrientesBG.getRowCount();i++){
        valor = ((Double.parseDouble(jtPasivosCorrientesBG.getModel().getValueAt(i, 1).toString())*100)/p);
        jtPasivosCorrientesBG.getModel().setValueAt((df.format(valor)+"%"),i ,2);
        }
        
        for(int i=0;i<jtPasivosFijosBG.getRowCount();i++){
        valor = ((Double.parseDouble(jtPasivosFijosBG.getModel().getValueAt(i, 1).toString())*100)/p);
        jtPasivosFijosBG.getModel().setValueAt((df.format(valor)+"%"),i ,2);
        }
        
        for(int i=0;i<jtCapitalContableBG.getRowCount();i++){
        valor = ((Double.parseDouble(jtCapitalContableBG.getModel().getValueAt(i, 1).toString())*100)/p);
        jtCapitalContableBG.getModel().setValueAt((df.format(valor)+"%"),i ,2);
        }
        
//      lblPasivosCorrientesAV.setText("%"+());

        capital = capital.setScale(2, RoundingMode.HALF_UP);
        lblPasivoCapital.setText("$" + (saldoPasivosCorrientesBG.doubleValue()+saldoPasivosFijosBG.doubleValue()+saldoCapitalContableBG.doubleValue()));
        //--------------------------------------
        //Modificar Cuentas
        op.allCuentas(jTModificar, id_catalogo);


    }

