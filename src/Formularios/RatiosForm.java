    public void realizarTito() {
        jTabbedPane1.setEnabledAt(1, false);
        jTabbedPane1.setEnabledAt(2, false);

        try {
            Statement sentenc2 = con.conectar().createStatement();
            ResultSet result2 = sentenc2.executeQuery("SELECT idcuenta FROM cuenta WHERE catalogo_idcatalogo = " + idCatalogo.getText() + " and nombre like '%nven%'");

            while (result2.next()) {
                int idecatalogo = result2.getInt("idcuenta");
                if (idecatalogo >= 0) {

                    jTabbedPane1.setEnabledAt(1, true);
                    jTabbedPane1.setEnabledAt(2, true);

                    System.out.println("Hola! soy tito...");
                } else {
                    System.out.println("Error");
                }
            }

        } catch (SQLException ex) {
            System.out.println("Error de habilitacion de ratios : " + ex.getMessage());
        }
    }

