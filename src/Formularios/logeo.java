    public String establecerTextoUsuario(String usuario) {
        String titulo = "";

        if (usuario.equals("0")) {
            titulo = ("Ha inicidado sesi칩n como Administrador");
        } else {
            titulo = ("Ha inicidado sesi칩n como usuario sin credencial");
        }
        return titulo;
    }

    public void limpiar() {
        // txtIdEmpresa.setText("");
        txtPass.setText("");
        txtUsuario.setText("");
    }

    public void logIn() {
        if (txtPass.getText().equals("") || txtUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No ha ingresado todos los datos necesarios", "Error", JOptionPane.WARNING_MESSAGE);
            limpiar();
        } else {
            String id = txtUsuario.getText();
            String pwd = new String(txtPass.getPassword());
            //int id_empresa = Integer.parseInt(txtIdEmpresa.getText());
            String empresa = (String) listarEmpresas.getSelectedItem();

            setId_catalogo(op.logeo(id, pwd, empresa));

            if (getId_catalogo() != 0) {

                String tipo = "";

                tipo = op.obtenerTipoUsuario(id);

                if (tipo.equals("0")) {
                    new indexHome().setVisible(true);
                    indexHome.idCatalogoGlobal.setText("" + getId_catalogo());
                    
                    actualizarCuentas.actualizarDatos(getId_catalogo());
                    
                    indexHome.tipoUsuario.setText("0");
                    indexHome.jlLogueadoComo.setText(establecerTextoUsuario(indexHome.tipoUsuario.getText()));
                    indexHome.jlNombreEmpresa.setText(empresa);
                    indexHome.nombreUsuario.setText(id);
                    System.out.println(indexHome.nombreUsuario.getText());
                    
                    this.hide();

                } else {
                    indexHome boladito = new indexHome();
                    boladito.setVisible(true);
                    boladito.idCatalogoGlobal.setText("" + getId_catalogo());
                    
                    actualizarCuentas.actualizarDatos(getId_catalogo());
                    
                    boladito.jmLibroDiario.setVisible(false);
                    boladito.jmCierres.setVisible(false);
                    boladito.jmAsientos.setVisible(false);
                    boladito.jmCrearCuenta.setVisible(false);

                    boladito.tipoUsuario.setText("1");
                    boladito.jlLogueadoComo.setText(establecerTextoUsuario(indexHome.tipoUsuario.getText()));
                    boladito.jlNombreEmpresa.setText(empresa);
                    this.hide();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Verifique los Datos", "Error de Logueo", JOptionPane.ERROR_MESSAGE);
                limpiar();
            }
        }
    }

