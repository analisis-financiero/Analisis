    private void btnCrearEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearEmpresaActionPerformed
        // Crear Empresa:

        if (txtNombreEmpresa.getText().trim().isEmpty() || txtEmailEmpresa.getText().trim().isEmpty() || txtDireccionEmpresa.getText().trim().isEmpty()
                || txtNombresEncargado.getText().trim().isEmpty() || txtApellidosEncargado.getText().trim().isEmpty() || txtFechanacEncargado.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No ha ingresado todos los valores", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {

            if (JOptionPane.showConfirmDialog(null, "¬øHa corroborado los datos?", "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0) {
                encargado.setNombres(txtNombresEncargado.getText());
                encargado.setApellidos(txtApellidosEncargado.getText());
                encargado.setFechanac(txtFechanacEncargado.getText());
                empresa.setNombre(txtNombreEmpresa.getText());
                empresa.setEmail(txtEmailEmpresa.getText());
                empresa.setDireccion(txtDireccionEmpresa.getText());
                int tipo;
                String tipo2 = "Servicios";

                if (tipo2.equals(cbtipoEmpresa.getSelectedItem())) {
                    tipo = 0;
                } else {
                    tipo = 1;
                }

                yome.CrearEmpresaEncargado(empresa, encargado, 0, 0, tipo);

                txtNombreEmpresa.setText("");
                txtEmailEmpresa.setText("");
                txtDireccionEmpresa.setText("");
                txtNombresEncargado.setText("");
                txtApellidosEncargado.setText("");
                txtFechanacEncargado.setText("");
            }
        }
    }//GEN-LAST:event_btnCrearEmpresaActionPerformed

